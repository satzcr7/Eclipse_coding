
public class Demo {

	public static void main(String[] args) {
		
		
		int n =4;
		int row=1;
		int star=1;
		
		
		while(row<=n) {
			
			//print star
			int i=1;
			while(i<=star) {
				System.out.print("*");
				i++;
			}
			
			
			//next row prep
			row++;
			star++;
			System.out.println();
			
			
			
		}
		
		
		

	}

}
